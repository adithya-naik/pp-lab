from turtle import*
canvas = Screen()
canvas.setup(400,400)

def square():
    sarah = Turtle()
    sarah.forward(50)
    sarah.left(90)
    sarah.forward(50)
    sarah.left(90)
    sarah.forward(50)
    sarah.left(90)
    sarah.forward(50)
    sarah.left(90)
    canvas.exitonclick()

